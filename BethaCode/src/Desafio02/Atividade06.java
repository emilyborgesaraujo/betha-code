package Desafio02;

public class Atividade06 {
    public static void main(String[] args) {
        int i = 15;

        while (i<=35) {

            if (i%2!=0) {
                System.out.println("O quadrado do número " + i + " é igual a "+ i*i);
            }

            i++;
        }
    }
}
